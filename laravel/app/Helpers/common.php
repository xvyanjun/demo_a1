//---------------------------------------------------------------
function scs($xc){
  $arr=[];
  $xc=request()->$xc;
   foreach($xc as $c=>$v){
     if($v->isValid()){
        $lj=$v->store('uploads_s');
        $arr[$c]='/'.$lj;
     }
   }  
  return $arr;
} 
